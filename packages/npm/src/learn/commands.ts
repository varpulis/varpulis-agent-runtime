/**
 * Learn tier: Custom slash command generation.
 *
 * Generates .claude/commands/*.md workflow files from recurring
 * pattern detections, encoding corrected workflows so the agent
 * follows a structured process instead of looping.
 */

import type { Detection } from "../types.js";

/** A proposed custom slash command. */
export interface CommandProposal {
  /** Pattern that triggered this proposal. */
  pattern_name: string;
  /** Command name (becomes /command-name in Claude Code). */
  name: string;
  /** Human-readable description. */
  description: string;
  /** Markdown content for the command file. */
  content: string;
  /** File path relative to project root. */
  file_path: string;
  /** Stable key for deduplication. */
  dedup_key: string;
}

