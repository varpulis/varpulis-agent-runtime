// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Generate a command proposal from a detection.
 * Returns null if no command template exists for this pattern.
 */
export function proposeCommand(detection: Detection): CommandProposal | null {
  const template = COMMAND_TEMPLATES[detection.pattern_name];
  if (!template) return null;
  return template(detection);
}

/**
 * Check if a command already exists by looking for the dedup key
 * in existing command file names or a list of known command names.
 */
export function isCommandDuplicate(
  proposal: CommandProposal,
  existingCommandNames: string[],
): boolean {
  return existingCommandNames.some(
    (name) =>
      name === proposal.name ||
      name === `${proposal.name}.md` ||
      name === proposal.file_path,
  );
}
